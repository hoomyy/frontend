export function getTranslation(key: string, language: Language, params?: Record<string, any>): string {
  let translation = translations[language]?.[key] || translations.fr[key] || key;
  
  if (params) {
    // Handle pluralization
    translation = translation.replace(/\{(\w+), plural, =1 \{([^}]+)\} other \{([^}]+)\}\}/g, (match, paramKey, singular, plural) => {
      const value = params[paramKey];
      return value === 1 ? singular : plural;
    });
    
    // Handle simple parameter replacement
    translation = translation.replace(/\{(\w+)\}/g, (match, paramKey) => {
      return params[paramKey]?.toString() || match;
    });
  }
  
  return translation;
}

export function t(key: string, params?: Record<string, any>): string {
  const language = (localStorage.getItem('hoomy_lang') as Language) || 'fr';
  return getTranslation(key, language, params);
}

